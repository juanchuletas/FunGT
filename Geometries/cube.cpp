void Cube::InstancedDraw(Shader& shader, int instanceCount) {
    texture.active();
    texture.bind();
    m_vao.bind();
    glDrawArraysInstanced(GL_TRIANGLES, 0, getNumOfVertices(), instanceCount);
    m_vao.unbind();
}